'use strict';

class MathUtil {
    static add (a, b) {
        return a + b;
    }

    static subtract (b, a) {
        return b - a;
    }

    static multiply (a, b) {
        return a * b;
    }

    static divide (a, b) {
        return a / b;
    }

    static modulo (a, b) {
        return a % b;
    }
}

module.exports = MathUtil;
